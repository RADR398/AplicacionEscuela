    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(Validacion() && Contraseña()){
        
           Trabajador t = new Trabajador();
           t.setNombre(nombre.getText());
           t.setApellido(apellido.getText());
           t.setDireccion(direccion.getText());
           t.setTelefono(telefono.getText());
           t.setNumeroDeCedula(cedula.getText());
           Rol rol = new Rol();
           rol.setIdRol(Rol.getSelectedIndex());
           t.setRol(rol);
           
           Dao.PermisosUsuario(Usuario.getText(),contraseña.getText().trim() , Rol.getSelectedItem().toString());
           Dao.SaveOrUpdateTrabajador(t);
           Vacio();
           
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public boolean Contraseña(){
    
        if(contraseña.getText().trim().equals(confirmar.getText().trim()) && !confirmar.getText().trim().equals("")
                && !contraseña.getText().trim().equals("")){
        
            return true;
        
        }else{
        
            JOptionPane.showMessageDialog(null, "Existe un error con la contraseña por favor verificar", "Error de contraseña", 
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
    
    }
    
    public boolean Vacio(){
    
        if(nombre.getText().trim().equals("") || apellido.getText().trim().equals("") || cedula.getText().trim().equals("-      -") ||
                telefono.getText().trim().equals("-") || direccion.getText().trim().equals("")){
            
            JOptionPane.showConfirmDialog(null, "Uno de los campos se encuentra vacio", "Error de campos vacios", JOptionPane.ERROR_MESSAGE);
            
            return false;
        
        }else{
            if(estado.getSelectedIndex()==0 || sangre.getSelectedIndex()==0 || Rol.getSelectedIndex()==0){
            
                JOptionPane.showConfirmDialog(null, "Falta gestionar unos de los campos de seleccion", "Error campos de seleccion", JOptionPane.ERROR_MESSAGE);
            return false;
            }
            
        return true;
        }
                        
    }
    
    public boolean Validacion(){
        
        Validar v = new Validar();
        return (Vacio() && v.Cedula(cedula.getText()) && v.ValidarLetras(nombre.getText(), "Nombre") && 
                v.ValidarLetras(apellido.getText(), "Apellido"));
        
    }
