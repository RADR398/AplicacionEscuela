    private void confirmarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_confirmarKeyReleased
       
        String confirmartx,contraseñatx,name, url = new File("").getAbsolutePath() +"//src//Image//";
        
        confirmartx = confirmar.getText().trim();
        contraseñatx = contraseña.getText().trim();
        
        if(!(confirmartx.equals("") || contraseñatx.equals(""))){
            
            if(confirmartx.equals(contraseñatx)){

                name = "Bueno.png";

            }else{

                name = "Malo.png";

            }

            animacion.setIcon(new ImageIcon(url+name));
        
        }else{
        
           animacion.setIcon(null);
        
        }
        
        
        
    }//GEN-LAST:event_confirmarKeyReleased

